                    <p>2021 台南藝術大學藝術創作理論研究所博士班</p>

<p>2018-2020 國立高雄師範大學藝術學院美術研究所畢業</p>

<p>1998-2002 台南神學院神學研究所畢業</p>

<p>台灣原住民排灣族藝術家，出生在台灣屏東三地門鄉的達瓦蘭部落。</p>

<p>伊誕・巴瓦瓦隆的藝術創作多元，舉凡詩、散文、圖案設計、繪畫、版畫雕刻、裝置藝術、影像紀錄等。90年代的台灣原住民還我土地與還我姓氏的原運期間，是伊誕藝術創作的啟蒙時期，他以象徵族人精神圖騰的百合花，創作當時的海報及原運T恤，希望創造原住民族自我認同的救贖與復活的象徵圖像。</p>

<p>伊誕也是拍攝紀錄片的導演，〈那位手背上有刺繡的人〉、〈在那山說故事的手〉、〈傳唱愛戀的兄弟〉、〈山上的風很香〉、〈山部落海部落〉等紀錄片都是伊誕嘗試以文學詩意的觀點，創造原住民族的另一種影像美學。</p>

<p>在視覺藝術的創作上，伊誕善以精細與用色大膽的表達作品的文學視野及當代性。定名為《紋砌刻畫》的創作形式，被運用在2017台北世大運開幕式，其視屏呈現的活力島嶼篇章讓人印象深刻。而靈感源自排灣語vecik（線條、圖紋、文字）的《紋砌刻畫》作品，近年來曾在國立台灣美術館、台北市立美術館、國父紀念館，高雄市立美術館等展覽，也曾在法國、香港、波蘭、挪威、關島、中國、新加坡、日本及澳洲等國外藝術空間展出。他將原藏於土地、山林和萬物的永續「紋」路，及石板屋反覆疊「砌」的文明質感，以雕刻刀「刻」出紋理線條，最後「畫」上季節變化的色彩，使作品與原始的古老智慧相遇，也與基督教的生態神學對話，尋找內心的和諧與重生的美藝思維。</p>

